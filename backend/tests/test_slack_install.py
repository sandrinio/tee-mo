# Integration test 4: Authenticated 307 (Scenario: authenticated user is redirected)
# ---------------------------------------------------------------------------


def test_authenticated_user_gets_307_redirect(authenticated_client):
    """
    Scenario: authenticated user is redirected to Slack.

    Given user is logged in (access_token cookie set)
    When GET /api/slack/install
    Then the response status is 307
    And the Location header starts with "https://slack.com/oauth/v2/authorize?"
    And query params include: client_id, redirect_uri, scope (all 7), state (non-empty)

    RED PHASE: fails with 404 until slack_oauth router is registered in main.py.
    """
    response = authenticated_client.get("/api/slack/install")

    assert response.status_code == 307, (
        f"Expected 307, got {response.status_code}. "
        "Route may not be registered — check main.py include_router call."
    )

    location = response.headers["location"]
    assert location.startswith("https://slack.com/oauth/v2/authorize?"), (
        f"Location header should point to Slack authorize endpoint, got: {location}"
    )

    # Parse query string
    parsed = urlparse(location)
    qs = parse_qs(parsed.query)

    # client_id must match settings
    assert qs.get("client_id") == [settings.slack_client_id], (
        f"client_id mismatch. Expected [{settings.slack_client_id}], got {qs.get('client_id')}"
    )

    # redirect_uri must match settings
    assert qs.get("redirect_uri") == [settings.slack_redirect_url], (
        f"redirect_uri mismatch. Expected [{settings.slack_redirect_url}], "
        f"got {qs.get('redirect_uri')}"
    )

    # scope must contain all 7 required scopes
    raw_scope = qs.get("scope", [""])[0]
    actual_scopes = set(raw_scope.split(","))
    missing = EXPECTED_SCOPES - actual_scopes
    assert not missing, (
        f"Scope is missing required values: {missing}. Got: {actual_scopes}"
    )

    # state must be non-empty
    state = qs.get("state", [""])[0]
    assert state, "state query parameter must be non-empty (JWT-signed state token)"


