# Unit test 3: State token tamper (Scenario: state token tamper detection)
# ---------------------------------------------------------------------------


def test_state_token_tamper():
    """
    Scenario: state token tamper detection.

    Given a valid state token with one character of the signature segment flipped
    When verify_slack_state_token() is called
    Then it raises jwt.InvalidTokenError (or a subclass: DecodeError, InvalidSignatureError)

    Splits on '.' and mutates the last character of the third segment — the
    same technique as test_security.py::test_decode_token_rejects_tampered_signature.

    RED PHASE: fails with ImportError until Green phase.
    """
    token = create_slack_state_token("alice-uuid")
    head, body, sig = token.split(".")
    # Flip the last character of the signature
    flipped = "A" if sig[-1] != "A" else "B"
    tampered = f"{head}.{body}.{sig[:-1]}{flipped}"

    with pytest.raises(jwt.InvalidTokenError):
        verify_slack_state_token(tampered)


