# Integration test 6: No plaintext user_id in Location header
# ---------------------------------------------------------------------------


def test_location_header_does_not_contain_plaintext_user_id(authenticated_client):
    """
    Scenario: Location header does NOT contain plaintext user_id.

    Given an authenticated user with id containing "alice-uuid-fixture"
    When GET /api/slack/install returns a 307
    Then the Location header does NOT contain the literal string "alice-uuid-fixture"

    The state parameter should be a JWT-signed opaque token, not a raw user_id.
    This test guards against accidentally embedding the user_id in plaintext in
    the redirect URL (e.g., as a state=alice-uuid-fixture query param).

    The authenticated_client fixture uses UUID a11ce000-f1x7-0000-0000-a11ce0fixture
    which contains "a11ce0fixture" — we assert neither this nor "alice-uuid-fixture"
    (the user_id string form) appears literally in the Location header.

    RED PHASE: fails with 404 until slack_oauth router is registered.
    """
    # Use a recognizable user_id that would be obvious if leaked
    recognizable_id = "alice-uuid-fixture"

    # Create a token for a user whose sub claim contains the recognizable string
    # We inject via a fresh client with a manually constructed access token
    # using jwt.encode directly to set a recognizable sub claim
    import jwt as jwt_module
    payload = {
        "sub": recognizable_id,
        "role": "authenticated",
        "iat": int(time.time()),
        "exp": int(time.time()) + 900,
    }
    token = jwt_module.encode(
        payload, settings.supabase_jwt_secret, algorithm=settings.jwt_algorithm
    )

    fresh_client = TestClient(app, follow_redirects=False)
    fresh_client.cookies.set("access_token", token)

    response = fresh_client.get("/api/slack/install")

    assert response.status_code == 307, (
        f"Expected 307 but got {response.status_code} — route may not be registered"
    )

    location = response.headers["location"]
    assert recognizable_id not in location, (
        f"Plaintext user_id '{recognizable_id}' found in Location header: {location}. "
        "The state must be JWT-encoded, not a raw user_id."
    )
