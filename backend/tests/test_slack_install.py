# Unit test 2: State token expiry (Scenario: state token expires after 5 minutes)
# ---------------------------------------------------------------------------


def test_state_token_expiry():
    """
    Scenario: state token expires after 5 minutes.

    Given a state token with iat = now - 301 seconds (1 second past the 300s window)
    When verify_slack_state_token() is called on it
    Then it raises jwt.ExpiredSignatureError

    Uses the now= keyword argument on create_slack_state_token per spec §3.3.
    Does NOT use freezegun — passes a pre-shifted timestamp directly.

    RED PHASE: fails with ImportError until Green phase.
    """
    # Shift iat 301 seconds into the past so exp = past_iat + 300 is already expired
    past_iat = int(time.time()) - 301
    token = create_slack_state_token("alice-uuid", now=past_iat)

    with pytest.raises(jwt.ExpiredSignatureError):
        verify_slack_state_token(token)


