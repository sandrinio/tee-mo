# Unit test 1: State token round-trip (Scenario: state token round-trips)
# ---------------------------------------------------------------------------


def test_state_token_round_trip():
    """
    Scenario: state token round-trips.

    Given a state token generated by create_slack_state_token(user_id="alice-uuid")
    When verify_slack_state_token() is called on it
    Then it returns SlackInstallState(user_id="alice-uuid", exp=<future int>)

    RED PHASE: fails with ImportError until create_slack_state_token,
    verify_slack_state_token, and SlackInstallState exist in Green phase.
    """
    token = create_slack_state_token("alice-uuid")
    state = verify_slack_state_token(token)

    assert isinstance(state, SlackInstallState)
    assert state.user_id == "alice-uuid"

    # exp must be in the future (token has a 300-second window)
    now = int(time.time())
    assert state.exp > now
    # exp should be approximately now + 300 (allow ±10s tolerance)
    assert abs(state.exp - (now + 300)) < 10


